  function pilih_metode(id) {
    $(".button-action-payment>ul").map(function() {
      if ($(this).children().length == 0) {
        $(this).parent().parent().remove();
      }
    });
    $("input[type='radio'][name='metode']").change(function() {
      if ($(this).is(":checked")) {
        $("li.active").removeClass("active")
        $(this).parent('li').addClass("active")
        $("#input-metode").val(id);
        $("#input-harga").val($("#payment-" + id).attr('data-harga'));
        $.ajax({
          url: "https://mogame.my.id/ajax/metode.php",
          type: 'POST',
          dataType: 'JSON',
          data: 'metode_id=' + id,
          success: function(result) {
            for (var i = 0; i < result.length; i++) {
              $("#mdu-metode").text(result[i].metode);
            }
          }
        });
      }
    })
  }

